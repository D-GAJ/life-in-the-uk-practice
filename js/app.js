// Timeline
function renderTimeline(filter = '') {
    els.timelineList.innerHTML = '';
    const filtered = importantDates.filter(d => 
        d.year.toLowerCase().includes(filter.toLowerCase()) || 
        d.event.toLowerCase().includes(filter.toLowerCase())
    );
    
    if(filtered.length === 0) {
        els.timelineList.innerHTML = '<p>No events found.</p>';
        return;
    }
    
    filtered.forEach(d => {
        const item = document.createElement('div');
        item.className = 'timeline-item glass-panel';
        item.innerHTML = `
            <div class="tl-year">${d.year}</div>
            <div class="tl-event">${d.event}</div>
        `;
        els.timelineList.appendChild(item);
    });
}

