// Study Plan Logic
function renderStudyPlan() {
    if (appState.examDate && appState.studyPlan) {
        els.planSetup.style.display = 'none';
        els.planActive.style.display = 'block';
        
        const today = new Date();
        today.setHours(0,0,0,0);
        const examDate = new Date(appState.examDate);
        examDate.setHours(0,0,0,0);
        
        const diffTime = examDate - today;
        const diffDays = Math.max(0, Math.ceil(diffTime / (1000 * 60 * 60 * 24)));
        els.daysRemainingNumber.innerText = diffDays;
        
        // Progress
        let totalTasks = 0;
        let completedTasks = 0;
        appState.studyPlan.forEach(day => {
            totalTasks += day.tasks.length;
            completedTasks += day.tasks.filter(t => t.completed).length;
        });
        const progress = totalTasks > 0 ? (completedTasks / totalTasks) * 100 : 0;
        els.planProgressFill.style.width = progress + '%';
        
        // Render List
        els.dailyScheduleList.innerHTML = '';
        appState.studyPlan.forEach((day, dayIndex) => {
            const planDate = new Date(day.date);
            const isPast = planDate < today;
            const isToday = planDate.getTime() === today.getTime();
            
            day.tasks.forEach((task, tIndex) => {
                const item = document.createElement('div');
                item.className = `timeline-item ${task.completed ? 'completed' : ''} ${isToday ? 'today' : ''}`;
                const dateStr = isToday ? 'Today' : planDate.toLocaleDateString(undefined, {month: 'short', day: 'numeric'});
                item.innerHTML = `
                    <div class="tl-year" style="font-size:1.2rem;">Day ${dayIndex+1}</div>
                    <div class="tl-event">
                        <strong>${dateStr}</strong>: ${task.label}
                    </div>
                `;
                els.dailyScheduleList.appendChild(item);
            });
        });
    } else {
        els.planSetup.style.display = 'block';
        els.planActive.style.display = 'none';
    }
}

function generateStudyPlan() {
    const dateVal = els.examDateInput.value;
    if (!dateVal) return alert('Please select a date');
    
    const examDate = new Date(dateVal);
    examDate.setHours(0,0,0,0);
    const today = new Date();
    today.setHours(0,0,0,0);
    
    const diffTime = examDate - today;
    const diffDays = Math.ceil(diffTime / (1000 * 60 * 60 * 24));
    
    if (diffDays <= 0) return alert('Exam date must be in the future.');
    
    const testKeys = Object.keys(questionsDB);
    const plan = [];
    
    // Distribute tests
    // Reserve last 2 days for Mock Exams
    const prepDays = Math.max(1, diffDays - 2);
    const testsPerDay = Math.ceil(testKeys.length / prepDays);
    
    let testIdx = 0;
    for(let i=0; i<diffDays; i++) {
        const currentDate = new Date(today);
        currentDate.setDate(today.getDate() + i);
        
        const dayTasks = [];
        
        if (i >= diffDays - 2) {
            dayTasks.push({ type: 'mock', label: 'Take 2 Mock Exams', completed: false });
            dayTasks.push({ type: 'flashcard', label: 'Review Flashcards', completed: false });
        } else {
            for(let j=0; j<testsPerDay && testIdx < testKeys.length; j++) {
                dayTasks.push({ type: 'test', testId: testKeys[testIdx], label: `Complete ${testKeys[testIdx]}`, completed: false });
                testIdx++;
            }
            if (i % 3 === 0) {
                dayTasks.push({ type: 'flashcard', label: 'Review 10 Flashcards', completed: false });
            }
        }
        
        plan.push({
            date: currentDate.getTime(),
            tasks: dayTasks
        });
    }
    
    appState.examDate = dateVal;
    appState.studyPlan = plan;
    localStorage.setItem('uk_test_examDate', dateVal);
    localStorage.setItem('uk_test_studyPlan', JSON.stringify(plan));
    
    renderStudyPlan();
    populateTestSelector();
}

function checkOffPlanTask(type, testId = null) {
    if(!appState.studyPlan) return;
    const today = new Date();
    today.setHours(0,0,0,0);
    
    let planUpdated = false;
    appState.studyPlan.forEach(day => {
        const planDate = new Date(day.date);
        // Only check off today or past tasks that match
        if (planDate <= today) {
            day.tasks.forEach(task => {
                if (!task.completed) {
                    if (type === 'test' && task.type === 'test' && task.testId === testId) {
                        task.completed = true;
                        planUpdated = true;
                    } else if (type === 'mock' && task.type === 'mock') {
                        task.completed = true;
                        planUpdated = true;
                    }
                }
            });
        }
    });
    
    if (planUpdated) {
        localStorage.setItem('uk_test_studyPlan', JSON.stringify(appState.studyPlan));
        renderStudyPlan();
        populateTestSelector();
    }
}


function getDailyTestRecommendation() {
    if (!appState.studyPlan || !appState.studyPlan.active) return [];
    
    const allKeys = Object.keys(questionsDB);
    
    // Prioritize Mock Exams, then Chapters
    const sortedKeys = allKeys.sort((a, b) => {
        const isMockA = a.startsWith('Mock');
        const isMockB = b.startsWith('Mock');
        if (isMockA && !isMockB) return -1;
        if (!isMockA && isMockB) return 1;
        return 0;
    });
    
    const uncompleted = sortedKeys.filter(k => !appState.studyPlan.completedTests.includes(k));
    
    return uncompleted.slice(0, appState.studyPlan.testsPerDay || 2);
}


function startRandomExam() {
    const mockKeys = Object.keys(questionsDB).filter(k => k.startsWith("Mock Exam"));
    if (mockKeys.length === 0) return alert("No Mock Exams available.");
    const randomKey = mockKeys[Math.floor(Math.random() * mockKeys.length)];
    startTest(questionsDB[randomKey], randomKey);
}

